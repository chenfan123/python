# 列表 元祖 字符串都可以视为序列。即内容连续、有序、可使用下标索引的一类数据容器

# 切片：从一个序列中取出一个子序列
# 语法：序列[起始下标，结束下标，步长]
# 起始下标表示从何处开始，留空表示从头开始
# 结束下标（不含）表示从何处结束，留空就是到末尾
# 步长n表示每次跳过n-1个元素取
# 步长为负数表示反向取（起始下标和结束下标也要反向标记）

list = ['a', 'c', 'b', 'd', 'e', 'f', 'g', 'h']
print(list[0:3:1])  # ['a', 'c', 'b']
print(list[0:3:2])  # ['a', 'b']
print(list[0:3])  # ['a', 'c', 'b']

print(list[::-1])  # ['h', 'g', 'f', 'e', 'd', 'b', 'c', 'a']
print(list[3:1:-1])  # ['d', 'c']
